import 'package:flutter_zitherharp/flutter_zitherharp.dart';

/// An [EdgeInsets] with zero offsets in each direction.
const EdgeInsets edgeInsets0 = EdgeInsets.zero;

/// Creates insets where all the offsets are `8`.
const EdgeInsets edgeInsets8 = EdgeInsets.all(8);

/// Creates insets where all the offsets are `16`.
const EdgeInsets edgeInsets16 = EdgeInsets.all(16);

/// Creates insets with only top is `4`.
const EdgeInsets edgeInsetsTop4 = EdgeInsets.only(top: 4);

/// Creates insets with only bottom is `48`.
const EdgeInsets edgeInsetsBottom48 = EdgeInsets.only(bottom: 48);

/// Creates insets with symmetrical vertical offset is `8`.
const EdgeInsets edgeInsetsVertical8 = EdgeInsets.symmetric(vertical: 8);

/// Creates insets with symmetrical horizontal offset is `8`.
const EdgeInsets edgeInsetsHorizontal8 = EdgeInsets.symmetric(horizontal: 8);

/// Creates insets with only left is `16` and right is `8`.
const EdgeInsets edgeInsetsLeft16Right8 = EdgeInsets.only(left: 16, right: 8);
